    void carregarProduto(String codigo) {
     CadProduto cadProduto = new CadProduto();
     ArrayList<AtributoCadastrarProduto> produto= new ArrayList<>();
     produto = cadProduto.buscarProduto(codigo);
     
     
    // SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
     //SimpleDateFormat sdf2 = new SimpleDateFormat("dd/MM/yyyy");
     //Date data = null;   
    
     
     txtNome.setText(produto.get(0).getNome());
     txtCodigo.setText(produto.get(0).getCodigo());
     txtCodigoPersonalizado.setText(produto.get(0).getCodigoPersonalizado());
     cbCategoria.setSelectedIndex(Integer.parseInt(produto.get(0).getCategoria()));
     if(produto.get(0).getMedida()==1)rbUn.setSelected(true);
     if(produto.get(0).getMedida()==2)rbKg.setSelected(true);
     if(produto.get(0).getMedida()==3)rbLt.setSelected(true);
     txtPrecoCusto.setText(produto.get(0).getPrecoCusto());
     txtPrecoVenda.setText(produto.get(0).getPrecoVenda());
     txaDetalhes.setText(produto.get(0).getDetalhes());
     txtEstoqueMin.setText(produto.get(0).getEstoqueMin());
     txtEstoqueAtual.setText(produto.get(0).getEstoqueAtual());
     ckbEstoque.setSelected(produto.get(0).isControlaEstoque());
     
        
        
        
    }
