    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        AtributoCadastrarProduto acp = new AtributoCadastrarProduto();
        CadProduto cadProduto = new CadProduto();
        
        int medida=0;
        if(rbUn.isSelected())medida=1;
        if(rbKg.isSelected())medida=2;
        if(rbLt.isSelected())medida=3;
        
        acp.setNome(txtNome.getText());
        acp.setCodigo(txtCodigo.getText());
        acp.setCodigoPersonalizado(txtCodigoPersonalizado.getText());
        acp.setCategoria(String.valueOf(cbCategoria.getSelectedIndex()));
        acp.setMedida(medida);
        acp.setPrecoCusto(txtPrecoCusto.getText());
        acp.setPrecoVenda(txtPrecoVenda.getText());
        acp.setDetalhes(txaDetalhes.getText());
        acp.setEstoqueMin(txtEstoqueMin.getText());
        acp.setEstoqueAtual(txtEstoqueAtual.getText());
        acp.setControlaEstoque(ckbEstoque.isSelected());
        cadProduto.cadProduto(acp);
        









    }//GEN-LAST:event_btnSalvarActionPerformed

